function mapMessages(
  base: Messages,
  transform: (value: string) => string,
  overrides: Partial<Messages> = {},
): Messages {
  const next = { ...base };
  for (const key of Object.keys(base) as (keyof Messages)[]) {
    next[key] = overrides[key] ?? transform(base[key]);
  }
  return next;
}

