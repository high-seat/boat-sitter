export function normalizeLanguageCode(value: string) {
  const normalized = value.trim().toLowerCase().replaceAll("_", "-");
  if (normalized === "en-gb" || normalized.startsWith("en-gb-")) return "en-GB";
  if (normalized === "en-us" || normalized.startsWith("en-us-")) return "en-US";
  if (normalized === "en" || normalized.startsWith("en-")) return "en-US";
  const base = normalized.split("-")[0] ?? "en-US";
  return SUPPORTED_LANGUAGES.some((language) => language.code === base) ? base : "en-US";
}

export function getIntlLocale(language?: string) {
  const code = normalizeLanguageCode(language ?? "en-US");
  return SUPPORTED_LANGUAGES.find((item) => item.code === code)?.locale ?? "en-US";
}

void i18n
  .use(LanguageDetector)
  .use(initReactI18next)
  .init({
    resources: Object.fromEntries(
      Object.entries(resources).map(([code, translation]) => [code, { translation }]),
    ),
    fallbackLng: "en-US",
    supportedLngs: SUPPORTED_LANGUAGES.map((language) => language.code),
    nonExplicitSupportedLngs: false,
    keySeparator: false,
    interpolation: { escapeValue: false },
    detection: {
      order: ["localStorage", "navigator"],
      lookupLocalStorage: "harbourly-language",
      caches: ["localStorage"],
      convertDetectedLanguage: normalizeLanguageCode,
    },
  })
  .then(() => {
    document.documentElement.lang = normalizeLanguageCode(i18n.language);
  });

i18n.on("languageChanged", (language) => {
  document.documentElement.lang = normalizeLanguageCode(language);
});

